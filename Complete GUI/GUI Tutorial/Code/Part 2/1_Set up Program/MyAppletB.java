//The Java Event Handling Package
import java.awt.event.*;
//This one implements two EventListeners: Action Listener and Mouse Listener
public class MyAppletB extends java.applet.Applet implements ActionListener, MouseListener {
	//This is called whenever an action is performed
	public void actionPerformed(ActionEvent evt) {
	}
	//This is called whenever the mouse is clicked
	public void mouseClicked(MouseEvent evt) {
	}
	//This is called when a mouse button is pressed
	public void mousePressed(MouseEvent evt) {
	}
	//This is called whenever a mouse button is released
	public void mouseReleased(MouseEvent evt) {
	}
	//This is called whenever the cursor enters the applet
	public void mouseEntered(MouseEvent evt) {
	}
	//This is called whenever the cursor exits the applet
	public void mouseExited(MouseEvent evt) {
	}
}